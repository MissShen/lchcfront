import PageBar from 'src/components/layout/page-bar'
export default {
  /**
   * -----------------------------------------------------------------------------------------------------------------
   *                                         分页列表页面数据预定义
   * -----------------------------------------------------------------------------------------------------------------
   */
  data() {
    return {
      resData: this.metaData(),
    }
  },
  components: {PageBar},
  methods: {
    /**
     * -----------------------------------------------------------------------------------------------------------------
     *                                        分页列表页面初始化
     * -----------------------------------------------------------------------------------------------------------------
     */
    metaData() {
      return {
        pageNum: 1,
        pageSize: 20,
        total: 0,
        pages: 0,
        list: []
      }
    },
    refreshPage(type, val) {
      if (0 === type) {
        this.resData.pageSize = val;
      }
      if (1 === type) {
        this.resData.pageNum = val;
      }
      this.metaList();
    },
    /**
     * -----------------------------------------------------------------------------------------------------------------
     *                                                    查询事件
     * -----------------------------------------------------------------------------------------------------------------
     */
    handleQuery() {
      this.resData = this.metaData();
      this.metaList();
    }
  }
}
